void SkeletonConverter::convertOpenNIKinect (void* fromPtr, SSI_SKELETON &to) {	
		
#ifdef SSI_SKEL_OPENNI_ENABLED
	OpenNIKinect::SKELETON& from = *(ssi_pcast(OpenNIKinect::SKELETON, fromPtr));

	// Fubi skeleton is equal to the ssi one, so we can directly copy it per joint
	for (ssi_size_t j = 0; j < SSI_SKELETON_JOINT::NUM; ++j)
	{
		to[j][OpenNIKinect::JOINT_VALUES::POS_X] = from[j][SSI_SKELETON_JOINT_VALUE::POS_X];
		to[j][OpenNIKinect::JOINT_VALUES::POS_Y] = from[j][SSI_SKELETON_JOINT_VALUE::POS_Y];
		to[j][OpenNIKinect::JOINT_VALUES::POS_Z] = from[j][SSI_SKELETON_JOINT_VALUE::POS_Z];
		to[j][OpenNIKinect::JOINT_VALUES::POS_CONF] = from[j][SSI_SKELETON_JOINT_VALUE::POS_CONF];

		to[j][OpenNIKinect::JOINT_VALUES::ROT_X] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_X];
		to[j][OpenNIKinect::JOINT_VALUES::ROT_Y] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_Y];
		to[j][OpenNIKinect::JOINT_VALUES::ROT_Z] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_Z];
		to[j][OpenNIKinect::JOINT_VALUES::ROT_CONF] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_CONF];
						
		to[j][OpenNIKinect::JOINT_VALUES::LOCROT_X] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_REL_X];
		to[j][OpenNIKinect::JOINT_VALUES::LOCROT_Y] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_REL_Y];
		to[j][OpenNIKinect::JOINT_VALUES::LOCROT_Z] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_REL_Z];
		to[j][OpenNIKinect::JOINT_VALUES::LOCROT_CONF] = from[j][SSI_SKELETON_JOINT_VALUE::ROT_REL_CONF];
	}
#else 
	ssi_wrn("OpenNI skeleton disabled");
#endif
}

