	void SkeletonConverter::transform(ITransformer::info info,
		ssi_stream_t &stream_in,
		ssi_stream_t &stream_out,
		ssi_size_t xtra_stream_in_num,
		ssi_stream_t xtra_stream_in[]) {

		SSI_SKELETON *dstptr = ssi_pcast(SSI_SKELETON, stream_out.ptr);
		for (ssi_size_t k = 0; k < stream_in.num * _skeleton_meta_in.num; k++) {
			for (ssi_size_t i = 0; i < SSI_SKELETON_JOINT::NUM; i++) {
				for (ssi_size_t j = 0; j < SSI_SKELETON_JOINT_VALUE::NUM; j++) {
					(*dstptr)[i][j] = SSI_SKELETON_INVALID_JOINT_VALUE;
				}
			}
		}
		dstptr = ssi_pcast(SSI_SKELETON, stream_out.ptr);

		/*
		 * Convert skeleton
		 */
		switch (_skeleton_meta_in.type) {

		case SSI_SKELETON_TYPE::MICROSOFT_KINECT:
		{
			MicrosoftKinect::SKELETON *srcptr = ssi_pcast(MicrosoftKinect::SKELETON, stream_in.ptr);
			for (ssi_size_t k = 0; k < stream_in.num * _skeleton_meta_in.num; k++) {
				if (stream_in.dim == 240 * _skeleton_meta_in.num) //legacy mode, no quaternions
					convertMicrosoftKinectOld(srcptr++, *dstptr++);
				else
					convertMicrosoftKinect(srcptr++, *dstptr++);
			}
		}
			break;

		case SSI_SKELETON_TYPE::MICROSOFT_KINECT2:
		{
			KINECT2_SKELETON *srcptr = ssi_pcast(KINECT2_SKELETON, stream_in.ptr);
			for (ssi_size_t k = 0; k < stream_in.num * _skeleton_meta_in.num; k++) {
				convertMicrosoftKinect2(srcptr++, *dstptr++);
			}
		}
			break;

		case SSI_SKELETON_TYPE::OPENNI_KINECT:
		{
#ifdef SSI_SKEL_OPENNI_ENABLED
			OpenNIKinect::SKELETON *srcptr = ssi_pcast (OpenNIKinect::SKELETON, stream_in.ptr);
			for (ssi_size_t k = 0; k < stream_in.num * _skeleton_meta_in.num; k++) {				
				convertOpenNIKinect (*srcptr++, *dstptr++);				
			}
#else 
			ssi_wrn("OpenNI skeleton disabled");
#endif
		}
			break;


		case SSI_SKELETON_TYPE::XSENS_MVN:
		{
#ifdef SSI_SKEL_XSENS_ENABLED
			XsensMVNSP::SKELETON *srcptr = ssi_pcast (XsensMVNSP::SKELETON, stream_in.ptr);
			for (ssi_size_t k = 0; k < stream_in.num * 1 ; k++) {				
				convertXsensMVN (*srcptr++, *dstptr++);				
			}
#else 
			ssi_wrn("Xsens skeleton disabled");
#endif
		}
			break;

		};

		/*
		 * Merge skeleton with face data
		 */
		if (_face_meta_in.type == SSI_FACE_TYPE::MICROSOFT_KINECT2){
			if (xtra_stream_in_num == 2)
			{
				dstptr = ssi_pcast(SSI_SKELETON, stream_out.ptr);
				MicrosoftKinect2::HEADPOSE *srcptr2 = ssi_pcast(MicrosoftKinect2::HEADPOSE, xtra_stream_in[0].ptr);
				MicrosoftKinect2::FACEPOINTS3D *srcptr3 = ssi_pcast(MicrosoftKinect2::FACEPOINTS3D, xtra_stream_in[1].ptr);
				convertFaceMicrosoftKinect2(*srcptr3++, *srcptr2++, *dstptr++);
			}
		}
		else if (_face_meta_in.type == SSI_FACE_TYPE::MICROSOFT_KINECT || (_face_meta_in.type < 0 && _skeleton_meta_in.type == SSI_SKELETON_TYPE::MICROSOFT_KINECT)){
			//The < 0 stuff is a fallback, in case someone uses the old microsoftkinect provider without the fixed face meta. In this case we use the skeleton meta to decide.

			if (xtra_stream_in_num == 2)
			{
				dstptr = ssi_pcast(SSI_SKELETON, stream_out.ptr);
				MicrosoftKinect::HEADPOSE *srcptr2 = ssi_pcast(MicrosoftKinect::HEADPOSE, xtra_stream_in[0].ptr);
				MicrosoftKinect::FACEPOINTS3D *srcptr3 = ssi_pcast(MicrosoftKinect::FACEPOINTS3D, xtra_stream_in[1].ptr);
				convertFaceMicrosoftKinect(*srcptr3++, *srcptr2++, *dstptr++);
			}

		}



	}


